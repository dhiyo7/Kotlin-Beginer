fun main () {

    /* Tipe Data Angka Double */
    val uang = 10.5
    println("Ini Tipe data Double " + uang)

    /* Tipe Data Angka Int */
    val uang2 = 7
    println("Ini Tipe data Int "+ uang2)

    /* Contoh Operasi Aritmatika */
    println(uang + uang2)
    println(uang - uang2)
    println(uang * uang2)
    println(uang / uang2)
    println(uang % uang2)
    println(uang2 * 5 - 10)


}